// var   -> 現在はあまり使用されない
// let   -> 値の上書き可能
// const -> 値の上書き不可

let name = 'Tom';
name = 'Tim';
console.log('hello ' + name);


// Number, String, Boolean, Undefined, Null, Symbol

// 動的型付け言語　
// 変数名は同じでも、入れる値によって型が変化する

// string
let variable = 'str';
console.log(typeof variable);

// number
variable = 12;
console.log(typeof variable);

// boolean
variable = false;
console.log(typeof variable);

// undefined
variable = undefined;
console.log(typeof variable);

